package aes.modes;

/**
 * BlockCipherMode represents a block cipher mode of operation.
 */
public enum BlockCipherMode {
    /**
     * <p>
     * Electronic Codeblock (ECB) mode is the simplest method of encryption.
     * </p>
     *
     * <p>
     * A message is divided into blocks of equal size. Each block is encrypted separately.
     * </p>
     */
    ECB
}
